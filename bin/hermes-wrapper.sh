#!/usr/bin/env bash
# hermes-wrapper.sh , one-command install + run for hermes-agent with the
# HookBus publisher plugin pre-wired.
#
# First run:
#   - Clones NousResearch/hermes-agent (if not present)
#   - Creates a Python venv and pip-installs hermes in editable mode
#   - Scaffolds ~/.hermes/.env with the MiniMax / OpenAI / Anthropic API key
#     (reads from MINIMAX_API_KEY env, or prompts)
#   - Installs the HookBus publisher plugin from the bundled source (or skips
#     if HERMES_SHIM_SRC points at a directory that doesn't exist, so the user
#     can install via `hermes plugins install <git-url>` instead)
#
# Every run:
#   - Reads the current HookBus bearer token from the running bus container
#     (via docker exec), so a token regenerated by `docker compose down -v`
#     is picked up automatically
#   - Strips any stale HOOKBUS_TOKEN line from ~/.hermes/.env and
#     ~/hermes-agent/.env so the fresh shell value wins
#   - Exports HOOKBUS_URL, HOOKBUS_TOKEN, HOOKBUS_FAIL_MODE
#   - Execs the real hermes CLI with your args. The installer also creates
#     ~/.local/bin/hermes so users can type the normal `hermes` command.

set -euo pipefail

HERMES_DIR="${HERMES_DIR:-$HOME/hermes-agent}"
HOOKBUS_URL="${HOOKBUS_URL:-http://localhost:18800/event}"
HOOKBUS_FAIL_MODE="${HOOKBUS_FAIL_MODE:-open}"
HOOKBUS_CONTAINER="${HOOKBUS_CONTAINER:-hookbus}"
HOOKBUS_TOKEN_PATH_IN_CONTAINER="${HOOKBUS_TOKEN_PATH_IN_CONTAINER:-/root/.hookbus/.token}"
SHIM_SRC="${HERMES_SHIM_SRC:-}"

say()  { printf "\033[1;32m[hermes-wrapper]\033[0m %s\n" "$*" >&2; }
warn() { printf "\033[1;33m[hermes-wrapper]\033[0m %s\n" "$*" >&2; }
die()  { printf "\033[1;31m[hermes-wrapper] error:\033[0m %s\n" "$*" >&2; exit 1; }

# -------- first-run install --------------------------------------------------

need_install=0
[[ ! -d "$HERMES_DIR/.git" ]] && need_install=1
[[ ! -d "$HERMES_DIR/venv" ]] && need_install=1

if (( need_install )); then
    say "first run, installing hermes-agent into $HERMES_DIR"

    if [[ ! -d "$HERMES_DIR/.git" ]]; then
        say "cloning NousResearch/hermes-agent..."
        git clone --quiet --depth 1 https://github.com/NousResearch/hermes-agent.git "$HERMES_DIR"
    fi

    if [[ ! -d "$HERMES_DIR/venv" ]]; then
        say "creating venv + installing hermes (~3 min)..."
        python3 -m venv "$HERMES_DIR/venv"
    fi

    # shellcheck disable=SC1091
    source "$HERMES_DIR/venv/bin/activate"
    pip install -q --upgrade pip
    pip install -q -e "$HERMES_DIR"
    # Some hermes versions declare python-dotenv in requirements.txt but
    # pip install -e . does not always pull requirements.txt depending on
    # the pyproject.toml layout. Install it explicitly to be safe.
    pip install -q python-dotenv
    # If there is a requirements.txt, install the pinned versions too.
    if [[ -f "$HERMES_DIR/requirements.txt" ]]; then
        pip install -q -r "$HERMES_DIR/requirements.txt"
    fi

    if [[ ! -f "$HERMES_DIR/.env" ]] && [[ -f "$HERMES_DIR/.env.example" ]]; then
        cp "$HERMES_DIR/.env.example" "$HERMES_DIR/.env"
    fi

    # Install HookBus publisher plugin from local source if provided.
    # Otherwise the user installs via: hermes plugins install <git-url>.
    if [[ -n "$SHIM_SRC" && -f "$SHIM_SRC/__init__.py" ]]; then
        # hermes plugin discovery only scans ~/.hermes/plugins/ and ./.hermes/plugins/.
        say "installing HookBus publisher plugin from $SHIM_SRC"
        HERMES_USER_PLUGIN_DIR="$HOME/.hermes/plugins/hookbus-publisher"
        mkdir -p "$HERMES_USER_PLUGIN_DIR"
        cp "$SHIM_SRC/__init__.py"   "$HERMES_USER_PLUGIN_DIR/"
        [[ -f "$SHIM_SRC/plugin.yaml" ]] && cp "$SHIM_SRC/plugin.yaml" "$HERMES_USER_PLUGIN_DIR/"
    else
        warn "HERMES_SHIM_SRC not set or plugin source missing."
        warn "Install via:  hermes plugins install https://github.com/agentic-thinking/hookbus-publisher-hermes"
    fi

    # Seed the MiniMax key from env if the user exported one, else prompt
    if ! grep -qE '^MINIMAX_API_KEY=.+' "$HERMES_DIR/.env" 2>/dev/null; then
        if [[ -n "${MINIMAX_API_KEY:-}" ]]; then
            printf "\nMINIMAX_API_KEY=%s\n" "$MINIMAX_API_KEY" >> "$HERMES_DIR/.env"
            say "MINIMAX_API_KEY written to .env from environment"
        elif [[ -t 0 ]]; then
            read -rsp "[hermes-wrapper] MINIMAX_API_KEY (or any other provider key; press Enter to skip): " K
            echo
            if [[ -n "$K" ]]; then
                printf "\nMINIMAX_API_KEY=%s\n" "$K" >> "$HERMES_DIR/.env"
            fi
        fi
    fi

    say "install complete."
fi

# -------- every run: refresh token + launch ----------------------------------

# Strip any stale HOOKBUS_TOKEN lines from .env files so the value we export
# from docker always wins. This fixes the silent-401 after `compose down -v`.
for f in "$HOME/.hermes/.env" "$HERMES_DIR/.env"; do
    if [[ -f "$f" ]]; then
        sed -i.bak '/^HOOKBUS_TOKEN=/d' "$f" 2>/dev/null || true
        rm -f "${f}.bak" 2>/dev/null || true
    fi
done

# Read the current bus token from the docker container. If docker isn't
# available or the container isn't running, we leave HOOKBUS_TOKEN as-is
# (the user may have set it explicitly in the shell).
if [[ -z "${HOOKBUS_TOKEN:-}" ]] && command -v docker >/dev/null 2>&1; then
    if token="$(docker exec "$HOOKBUS_CONTAINER" cat "$HOOKBUS_TOKEN_PATH_IN_CONTAINER" 2>/dev/null)"; then
        HOOKBUS_TOKEN="$token"
    fi
fi

if [[ -z "${HOOKBUS_TOKEN:-}" ]]; then
    warn "HOOKBUS_TOKEN not set. Events will post without auth and may be rejected (401)."
    warn "Fix:  export HOOKBUS_TOKEN=\$(docker exec hookbus cat /root/.hookbus/.token)"
fi

export HOOKBUS_URL HOOKBUS_FAIL_MODE
[[ -n "${HOOKBUS_TOKEN:-}" ]] && export HOOKBUS_TOKEN

cd "$HERMES_DIR"
# shellcheck disable=SC1091
source venv/bin/activate
exec ./hermes "$@"
